const TRUNCATE_LENGTH = 200;

export default function Reviews() {
  const [expandedId, setExpandedId] = useState<number | null>(null);

  return (
    <section className={`section ${styles.reviews}`} id="reviews">
      <div className="section-container">
        <h2 className="section-title">Відгуки</h2>
        <div className={styles.grid}>
          {reviews.map((review) => {
            const isLong = review.text.length > TRUNCATE_LENGTH;
            const isExpanded = expandedId === review.id;
            const displayText =
              isLong && !isExpanded
                ? review.text.slice(0, TRUNCATE_LENGTH) + "..."
                : review.text;

            return (
              <div className={styles.card} key={review.id}>
                <div className={styles.header}>
                  <div className={styles.avatarWrapper}>
                    <Image
                      src={review.avatar}
                      alt={review.name}
                      width={48}
                      height={48}
                      className={styles.avatar}
                    />
                  </div>
                  <div>
                    <p className={styles.name}>{review.name}</p>
                  </div>
                </div>
                <p className={styles.text}>{displayText}</p>
                {isLong && (
                  <button
                    className={styles.toggle}
                    onClick={() =>
                      setExpandedId(isExpanded ? null : review.id)
                    }
                  >
                    {isExpanded ? "Згорнути" : "Читати далі"}
                  </button>
                )}
              </div>
            );
          })}
        </div>
      </div>
    </section>
  );
}